const normalizeWord = (word = '') =>
  word
    .replace(/[_]/g, ' ')
    .replace(/\s+/g, ' ')
    .replace(/[^A-Za-z'\-\s]/g, '')
    .trim()
const stopwords = new Set(['of', 'the', 'and', 'to', 'for', 'a', 'an', 'in', 'on', 'at', 'by'])

const uniqueWords = (...collections) => {
  const bucket = new Set()
  collections.flat().forEach((entry) => {
    const normalized = normalizeWord(entry)
    if (!normalized) {
      return
    }
    normalized
      .split(' ')
      .map((token) => token.trim())
      .filter(Boolean)
      .forEach((token) => {
        if (stopwords.has(token.toLowerCase())) {
          return
        }
        bucket.add(token)
      })
  })
  return Array.from(bucket)
}

const shuffleInPlace = (items) => {
  for (let i = items.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[items[i], items[j]] = [items[j], items[i]]
  }
}

const createCatalog = ({ prefixes, suffixes, limit, joiner = '' }) => {
  const combos = []
  const seen = new Set()
  for (const prefix of prefixes) {
    for (const suffix of suffixes) {
      const candidate = [prefix, suffix].filter(Boolean).join(joiner).replace(/\s+/g, ' ').trim()
      if (!candidate || seen.has(candidate)) {
        continue
      }
      seen.add(candidate)
      combos.push(candidate)
    }
  }
  shuffleInPlace(combos)
  return Object.freeze(combos.slice(0, limit))
}

const buildConnectorPool = (size, words) => {
  if (!size) {
    return []
  }
  const clean = words.map((word) => normalizeWord(word)).filter(Boolean)
  const pool = []
  for (let i = 0; pool.length < size; i += 1) {
    pool.push(clean[i % clean.length])
  }
  return pool
}

